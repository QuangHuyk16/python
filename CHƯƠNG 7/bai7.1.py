import tkinter as tk

def main():
    # Tạo một cửa sổ
    window = tk.Tk()

    # Đặt tiêu đề cho cửa sổ
    window.title("Welcome to Uneti app")
    #Đặt kích thước cửa sổ theo pixels 
    window.geometry('280x60') 
    # Chạy vòng lặp chính của cửa sổ
    window.mainloop()

if __name__ == "__main__":
    main()
